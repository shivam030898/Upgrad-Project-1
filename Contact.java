public class Contact{
    //Attributes
    private String name;
    private String number;
    private String email;
    
    public Contact(){

    }
    
    //Getter for name
    String name() {
        return name;
    }
    // Setter for name
    public void setname(String name) {
        this.name = name;
    }
      //Getter for number
      String number() {
        return number;
    }
    // Setter for number
    void setnumber(String number) {
        this.number = number;
    }
      //Getter for email
      String email() {
        return email;
    }
    // Setter for email
    void setemail(String email) {
        this.email = email;
    }
    
    
}